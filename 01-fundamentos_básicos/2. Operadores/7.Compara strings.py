# Compara strings

nombre1 = "Ana"
nombre2 = "ana"
nombre3 = "Ana"

# Evalúa:
print(nombre1 == nombre2)  # ¿Qué esperas?
print(nombre1 == nombre3)  # ¿Qué esperas?

# Compara ignorando mayúsculas:
print(nombre1.lower() == nombre2.lower())

# ¿Cuál es alfabéticamente mayor?
print("ana" > "Ana")  # ¿Qué esperas?

