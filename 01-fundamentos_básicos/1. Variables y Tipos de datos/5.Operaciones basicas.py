# Dados dos números, calcula:
a = 15
b = 4

# 1. Suma
# 2. Resta
# 3. Multiplicación
# 4. División normal
# 5. División entera
# 6. Módulo (resto)
# 7. Potencia (a elevado a b)
suma=a+b
resta=a-b
multiplicacion=a*b
division_normal=a/b
division_entera=a//b
modulo=a%b
potencia=a**b

print(f"Tengo dos números {a} y {b}")
print(f"La suma es: {suma}")
print(f"La resta es: {resta}")
print(f"La Multiplicación es: {multiplicacion}")
print(f"La division es: {division_normal}")
print(f"La division entera es: {division_entera}")
print(f"El modulo o resto es: {modulo}")
print(f"{a} elevado a {b} es: {potencia}")